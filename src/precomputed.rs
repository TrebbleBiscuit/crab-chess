fn calculate_distance_between_squares() -> [[u32; 64]; 64] {
    let mut distances = [[0u32; 64]; 64];
    for to_square_index in 0..64 {
        let row1 = to_square_index / 8;
        let col1 = to_square_index % 8;
        for from_square_index in 0..64 {
            let row2 = from_square_index / 8;
            let col2 = from_square_index % 8;
            let row_distance = (row1 as i32 - row2 as i32).abs();
            let col_distance = (col1 as i32 - col2 as i32).abs();
            let euclidean_distance = ((row_distance * row_distance + col_distance * col_distance)
                as f64)
                .sqrt()
                .round() as u32;
            distances[to_square_index][from_square_index] = euclidean_distance;
        }
    }
    distances
}

fn gen_file_masks() -> [u64; 8] {
    let file_a_mask = 0x0101010101010101u64;
    let mut file_masks: [u64; 8] = [0; 8];
    for n in 0..8 {
        file_masks[n] = file_a_mask << n
    }
    file_masks
}
